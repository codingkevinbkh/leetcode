# Definition for a binary tree node.
# class TreeNode:
#     def __init__(self, val=0, left=None, right=None):
#         self.val = val
#         self.left = left
#         self.right = right
class Solution:
    def invertTree(self, root: TreeNode) -> TreeNode:
        def invert(head):
            if head.left:
                invert(head.left)
            if head.right:
                invert(head.right)
            head.left, head.right = head.right, head.left
        
        if root:
            invert(root)
        return root
